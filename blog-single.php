						<h2 class="mb-3">BMW 2 Serie Gran Coupé vs Mercedes-Benz CLA.</h2>
            <p>La histórica rivalidad entre BMW y Mercedes-Benz se extiende a las berlinas compactas de corte coupé, un segmento en el que la casa de la estrella lleva presente desde 2013. En estos seis años, no ha aparecido un contrincante directo del CLA Coupé... hasta ahora.

  </p>
            <p>
              <img src="images/bmwvsmb2.jpg" alt="" class="img-fluid">
            </p>
            <p>El BMW Serie 2 Gran Coupé nace con el objetivo de hacerle la vida mucho más difícil a su enemigo. La receta es la misma para ambos: una carrocería baja y con un techo muy inclinado desde el pilar B hasta la zaga. Todo, complementado con cuatro puertas sin marcos y una tapa para acceder al maletero.</p>

            <h2 class="mb-3 mt-5">Inicio de la pelea entre estas dos grandes marcas</h2>
            <p>El CLA Coupé se encuentra en su segunda generación y lo podemos considerar un CLS a escala, aunque con elementos propios. Por su parte, para BMW, es el estreno en esta categoría, aunque no le falta experiencia en este tipo de vehículos. No hay más que ver el Serie 4 Gran Coupé o el Serie 6 Gran Turismo.</p>
            <p>
              <img src="images/bmw2.jpg" alt="" class="img-fluid">
            </p>
            <p>El Serie 2 Gran Coupé aterrizará en marzo de 2020 y los precios aún se desconocen. Por su parte, el Mercedes-Benz CLA Coupé tiene una tarifa base de 663,830 pesos mexicanos. </p>
            <p>En una comparativa estética, debemos resaltar que los frontales ofrecen herencias claras del Serie 1 y el Clase A, respectivamente. Ambos recurren un capó abombado y paragolpes de claro diseño deportivo. </p>
            <p>Por su parte, el perfil dibuja superficies acristaladas diferentes. La del Serie 2 Gran Coupé consigue dar la impresión de que el coche está 'levantado' en la parte trasera, mientras que la del CLA Coupé es más tradicional, con el clásico diseño arqueado.</p>
            <p>
              <img src="images/mb.jpg" alt="" class="img-fluid">
            </p>

            <p>Es más, en el modelo de Múnich se integra una tercera ventana lateral, siguiendo la tradición de la marca. De este modo, el conductor amplía ligeramente la visibilidad hacia ese ángulo, aunque el coche carece de unos hombros tan 'poderosos' como los del CLA Coupé. </p>

